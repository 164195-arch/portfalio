# โปรแกรมคำนวณพื้นที่รูปทรงหลายแบบ
# พัฒนาต่อยอดจากโค้ดต้นแบบ
# ============================================

print("=" * 40)
print("  โปรแกรมคำนวณพื้นที่รูปทรงเรขาคณิต")
print("=" * 40)

# วนซ้ำจนกว่าผู้ใช้จะเลือกออก
ทำงานต่อ = True

while ทำงานต่อ:

    # --- เมนูหลัก ---
    print("\nเลือกรูปทรงที่ต้องการคำนวณ:")
    print("  1. สี่เหลี่ยมมุมฉาก")
    print("  2. วงกลม")
    print("  3. สามเหลี่ยม")
    print("  4. ออกจากโปรแกรม")

    ตัวเลือก = input("\nกรุณาเลือก (1-4): ")

    # --- สี่เหลี่ยมมุมฉาก ---
    if ตัวเลือก == "1":
        print("\n[ สี่เหลี่ยมมุมฉาก ]")
        กว้าง = float(input("  กรอกความกว้าง: "))
        ยาว = float(input("  กรอกความยาว: "))
        พื้นที่ = กว้าง * ยาว
        print(f"  พื้นที่คือ {พื้นที่} ตารางหน่วย")

    # --- วงกลม ---
    elif ตัวเลือก == "2":
        print("\n[ วงกลม ]")
        รัศมี = float(input("  กรอกรัศมี: "))
        pi = 3.14159
        พื้นที่ = pi * รัศมี * รัศมี
        print(f"  พื้นที่คือ {round(พื้นที่, 2)} ตารางหน่วย")

    # --- สามเหลี่ยม ---
    elif ตัวเลือก == "3":
        print("\n[ สามเหลี่ยม ]")
        ฐาน = float(input("  กรอกความยาวฐาน: "))
        สูง = float(input("  กรอกความสูง: "))
        พื้นที่ = (ฐาน * สูง) / 2
        print(f"  พื้นที่คือ {พื้นที่} ตารางหน่วย")

    # --- ออกโปรแกรม ---
    elif ตัวเลือก == "4":
        print("\nขอบคุณที่ใช้งานโปรแกรม")
        ทำงานต่อ = False

    # --- กรณีกรอกผิด ---
    else:
        print("  กรุณาเลือกเฉพาะ 1, 2, 3 หรือ 4 เท่านั้น")